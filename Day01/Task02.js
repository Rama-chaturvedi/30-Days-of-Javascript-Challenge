let num = "Task 02";  // Declare and assign the value using 'let' keyword
console.log(num);     // logs the value of variable of the console

num = "30-Days of javascript challenge";        //   reassigns the variable
console.log(num);  

// note:
// Using 'let' keyword we declare, assign and re-assign the value.
// but we cann't re-declare the variable. 