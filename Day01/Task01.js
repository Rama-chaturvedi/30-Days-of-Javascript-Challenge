var num = 23;                  //declare a variable num and assign the value     
console.log(num);              // log the value of variable to the console

var num = "Day 1 challenge"       // Re-declare and Re-assign the value 
console.log(num);                 

num = "Task 01"             // assign a new value, without using 'var'  
console.log(num);

// Notes : 
// 1. We can re-declare and re-assign the value of a variable multiple times
// 2. We can also assign a new value to a variable without using 'var' keyword, for updating the existing variable.
 